from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

SCRIPT_DIR = Path(__file__).resolve().parent
if str(SCRIPT_DIR) not in sys.path:
    sys.path.insert(0, str(SCRIPT_DIR))

from memory_store import save_daily_result
from organize_daily import render_markdown
from validate_daily_result import assert_valid_result


def main() -> int:
    parser = argparse.ArgumentParser(description="Validate and store a generated DailyResult in the skill-owned memory store.")
    parser.add_argument("--input", required=True, help="DailyResult JSON path generated by the calling agent.")
    parser.add_argument("--memory-home", help="Local memory store directory.")
    parser.add_argument("--date", default="", help="Result date. Defaults to requestId when omitted.")
    parser.add_argument("--markdown-output", default="", help="Optional stable Markdown output for the final daily memory.")
    args = parser.parse_args()

    with Path(args.input).open("r", encoding="utf-8") as fh:
        result = json.load(fh)
    if not isinstance(result, dict):
        raise SystemExit("DailyResult must be a JSON object")
    assert_valid_result(result)
    path = save_daily_result(result, args.date, args.memory_home)
    if args.markdown_output:
        markdown_path = Path(args.markdown_output)
        markdown_path.parent.mkdir(parents=True, exist_ok=True)
        markdown_path.write_text(render_markdown(result), encoding="utf-8")
    print(f"OK: saved {path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
